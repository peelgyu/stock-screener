def search_kr_stocks(query: str) -> list[dict]:
    """한국어 검색어로 종목 검색"""
    query = query.strip()
    results = []

    # 한국 주식 검색
    for name, (ticker, eng_name) in KR_STOCKS.items():
        if query in name:
            results.append({
                "symbol": ticker,
                "name": name,
                "engName": eng_name,
                "exchange": "KOSDAQ" if ".KQ" in ticker else "KOSPI",
            })

    # 미국 주식 한국어 검색
    for kr_name, ticker in US_STOCKS_KR.items():
        if query in kr_name:
            results.append({
                "symbol": ticker,
                "name": kr_name,
                "engName": kr_name,
                "exchange": "US",
            })

    # 중복 제거 (같은 티커)
    seen = set()
    unique = []
    for r in results:
        if r["symbol"] not in seen:
            seen.add(r["symbol"])
            unique.append(r)

    return unique[:8]


